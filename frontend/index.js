/*jslint node: true */
"use strict";

var cluster = require("cluster");
var frontend = require("./frontend");

if (cluster.isMaster) {
    console.info("index: spawn child process");
    cluster.fork();
    cluster.on("exit", function () {
        setTimeout(function () {
            console.info("index: respawn child process");
            cluster.fork();
        }, 1000);
    });

} else {
    frontend.start();
}
